#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	int i;

	i = -1000000000;
	if (n == 0)
	{
		write(fd, "0", 1);
		return ;
	}
	if (n >= 0)
		n *= -1;
	else
		write(fd, "-", 1);
	while (n / i == 0)
		i /= 10;
	while (i)
	{
		ft_putchar_fd(n / i + '0', fd);
		n = n % i;
		i /= 10;
	}
}
