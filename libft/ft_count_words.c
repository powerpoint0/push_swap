#include "libft.h"

size_t	ft_count_words(char const *s, char c)
{
	size_t words;
	size_t i;

	i = 0;
	words = 0;
	while (s[i])
	{
		while (s[i] == c)
			i++;
		if (s[i])
		{
			words++;
			while (s[i] && s[i] != c)
				i++;
		}
	}
	return (words);
}
