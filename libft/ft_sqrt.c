#include "libft.h"

size_t	ft_sqrt(size_t nb)
{
	size_t sq;

	sq = 1;
	if (nb > 0)
	{
		while (sq * sq <= nb)
			sq += 1;
		if ((sq - 1) * (sq - 1) == nb)
			return (sq - 1);
	}
	return (0);
}
