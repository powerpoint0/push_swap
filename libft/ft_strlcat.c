#include "libft.h"

size_t	ft_strlcat(char *dst, const char *src, size_t size)
{
	size_t len;
	size_t i;

	len = ft_strlen(dst);
	if (size <= len)
		return (size + ft_strlen(src));
	i = 0;
	while (len + i < size - 1 && src[i])
	{
		dst[len + i] = src[i];
		i++;
	}
	dst[len + i] = '\0';
	return (len + ft_strlen(src));
}
