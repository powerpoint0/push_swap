#include "libft.h"

void	ft_putnbr(int n)
{
	int i;

	i = -1000000000;
	if (n == 0)
	{
		write(1, "0", 1);
		return ;
	}
	if (n >= 0)
		n *= -1;
	else
		write(1, "-", 1);
	while (n / i == 0)
		i /= 10;
	while (i)
	{
		ft_putchar(n / i + '0');
		n = n % i;
		i /= 10;
	}
}
